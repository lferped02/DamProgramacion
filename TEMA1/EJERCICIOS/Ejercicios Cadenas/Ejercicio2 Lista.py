diasSemana=["Lunes", "Martes", "Miercoles", "Jueves", "Viernes"]
print (diasSemana)
print (diasSemana[0:len(diasSemana)]) #Lista completa
print (diasSemana[1:3]) #Devuelve el elemento 1 y 2
print (diasSemana[1:]) #Devuelve el elemento 1 hasta el final
print (diasSemana[:4]) #Devuelve desde el principio hasta el anterior del 4
print (diasSemana[:]) #Te da la lista completa
print (diasSemana[-1]) #Accede al último elemento de la lista.
print (diasSemana[:len(diasSemana)]) #Igual que la primera línea: imprime desde el inicio hasta el final.
print (diasSemana[len(diasSemana)-1]) #Calcula el índice del último elemento
print (diasSemana[-1]) #Repetición del punto 3: imprime el último elemento.

